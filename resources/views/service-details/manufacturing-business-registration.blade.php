	    	<div class="row">
	    		<div class="col-lg-12">
	    			<div>
						As per the choices mentioned above of business categories of manufacturing, the process of obtaining the NTN is bifurcated as follows:
					</div>
					<div class="desc mb-20">
						<ul class="check-lists px-5">
							<li>NTN for other than companies</li>
							<li>NTN for Companies</li>
						</ul>
						Here is the explanation of what the difference is.

					</div>
					<div>
						<h3 class="title">NTN for other than companies</h3>
					</div>
	    			<div class="desc mb-20">
	    				For NTN registration of <b>Proprietorship, AOP/ Partnership Firm and Limited Liability Partnership Firm (LLP)</b>, the partner or member of the above mentioned will present before Income Tax Authorities after the completion of required documents;
	    			</div>
	    			<div>
						<h3 class="title">Documents required for NTN registration</h3>
					</div>
	    			<div class="desc mb-20">
	    				Requisite documents are listed below:
	    				<ol class="order-list px-5">
	    					<li>NTN Registration Form (TRF-01 Form)</li>
							<li>Business Email for NTN Registration, which is not used earlier in any other NTN</li>
							<li>Mobile Number of Proprietor/Partner/Member not being used earlier in any other NTN</li>
							<li>Letter of Authority in favour of one Partner/Member who would be Principle Partner/Member declared by other Partners/Members. No such requirement is needed in case of Proprietorship Business.</li>
							<li>Paid Electricity Bill (within the last three months from the date of Application for NTN )</li>
							<li>Original CNIC of Proprietor/Member/Partnerrepresenting the business.</li>
							<li>Original Certificate of Incorporation/Registration Certificate in case of Partnership Firm.</li>
							<li>Original Partnership Deed/Agreement in case of Partnership Firm Registered under Partnership Act 1932.</li>
							<li>Business Premises Title/ Rent Agreement along with Owner / ownership Documents or Tenancy/Lease / Landlord CNIC Copy</li>
	    				</ol>
	    			</div>
	    			<div class="desc mb-20">
	    				Gather all the above-required documents and visit the nearest RTO. The facilitation officer will verify your documents for satisfaction and will begin the registration process. Complete the 181 (form of registration filed voluntarily) (income tax). Scan and attach the documents mentioned above and submit the application. If verified, you shall be issued with a Login ID and Password via an SMS or email. Print out the form 181 from your account, log in with the provided ID and Password.
	    			</div>
	    			<div>
						<h3 class="title">NTN for Companies</h3>
					</div>
	    			<div class="desc mb-5">
	    				Registration Process of manufacturing Company is similar to other companies as they get registered. The difference between companies and other business categories for obtaining NTN is that a company does not need to follow the procedure mentioned above for NTN registration. <b>When a company gets itself registered with SECP, then SECP issues NTN to a company along with the issuance of an incorporation certificate.</b>
	    			</div>
	    			<div class="desc mb-20">
	    				However, for better understanding, here is a brief explanation on how to get an incorporation certificate for obtaining NTN in the case of companies.
	    				<ul class="check-lists px-5">
	    					<li>Approval of Name</li>
	    					<li>Reservation of Name and Incorporation of Company:</li>
	    				</ul>
	    			</div>
	    			<div>
						<h3 class="title">Approval of Company Name</h3>
					</div>
	    			<div class="desc mb-5">
	    				You are required to determine the company's name and trace out its availability on the SECP website at the time registering a company in Pakistan to avoid prohibited names. On the availability of name, you may reserve that company name (For sixty days) by establishing an online account with SECP.
	    			</div>
	    			<div>
	    				<h3 class="title">Reservation of Name and Incorporation of Company</h3>
	    			</div>
	    			<div class="desc mb-20">
	    				<ul class="check-lists">
	    					<li>Log in to the e-services page on the SECP website by going to the SECP website.</li>
							<li>You will be required a valid CNIC in case of a Pakistani National, and a valid passport in case of a foreigner.</li>
							<li>You can select 'fast track registration services' (FTRS) for availing same-day registration service.</li>
							<li>Online form is required to be filled and file it by adding your detail/particulars.</li>
							<li>Security codes via SMS and email will be sent which you will be required to write in the form.</li>
							<li>After verification of security codes, a PIN will be generated.</li>
							<li>The user registration fee can be paid online through your debit/credit card, or you may print out the challan. The amount can be deposited in any of the selected branches of UBL or MCB. The normal registration fee is Rs. 100/-</li>
	    				</ul>
	    			</div>
	    			<div class="desc mb-20">
	    				After that, you shall receive a confirmation email from SECP within a day. In case there is no prohibited word in the company name, the name will be reserved.
	    			</div>
	    			<div class="desc mb-20">
	    				There are following some documents which are mandatory for registration of a company in Pakistan.
	    			</div>
	    			<div>
	    				<h3 class="title">Documents required for Company Incorporation</h3>
	    			</div>
	    			<div class="desc mb-20">
	    				MOA and AOA their samples are available on the SECP's website. <br>
	    				MOA explains your company relationship with the outside world under which business sector your company, e.g. Institution, Travel agency etc.
	    				<ul class="check-lists px-5">
	    					<li>Articles of Association is about day-to-day proceedings within the company. It tells about the role of the directors and CEO. In fact, it tells about how the company will run/operate.</li>
							<li>Copies of CNIC of Directors, CEO, Nominee (for the single-member company) or copy of passport in case of a foreigner.</li>
							<li>If a foreign company is to be incorporated, you will be required to provide more detail such as the List and details of Directors and nationality of directors, the company profile etc. You also have to provide the certified copy of Company’s charter.</li>
							<li>NOC (No Objection Certificate) or letter of intent/ license from relevant authorities if you wish to register some sort of specialized business.</li>
							<li>Authorization of Subscribers is needed for the purpose of filing of documents.</li>
							<li>Registration/Filing fee.</li>
							<li>The company incorporation fee is Rs. 1,000/- for online submission if the nominal share capital is not more than Rs 100,000. For offline submission, the company incorporation fee is Rs. 2,000/-</li>
	    				</ul>
	    			</div>
	    			<div>
	    				<h3 class="title">Certificate of Incorporation and Company Seal</h3>
	    			</div>
	    			<div class="desc mb-20">
	    				On submission of all the documents, SECP will scrutinize them, and they will also check out the validity of all the documents. A company seal or digital signature will be allotted by the National Institutional Facilitation Center (NIFT). You can have it from SECP.
	    			</div>
	    			<div class="desc mb-20">
	    				All company directors' signatures are mandatory to receive the said company seal. <b>Certificate of incorporation from SECP will be received on authentication of all the provided documents and the certificate will be issued in either physical form or electronically along with the NTN.</b>
	    			</div>
