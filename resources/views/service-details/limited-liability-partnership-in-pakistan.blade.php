					<div class="desc mb-20">
	    				The Securities Exchange Commission of Pakistan regulates the incorporation process of an L.L.P. under Limited Liability Partnership Act, 2017. Here are few steps that need to be elaborated in order to get the L.L.P. registered in Pakistan:
	    				<ul class="check-lists px-5">
	    					<li>Selection of Name and User Registration on SECP Website</li>
 							<li>Application for Name Reservation</li>
							<li>Completion of documents required for Incorporation</li>
							<li>Fee payment</li>
							<li>Submission of documents</li>
							<li>Issuance of Incorporation Certificate</li>
